package Player;

public class Anyagok {
    private int ronk;
    private int ko;
    private int fu;
    private int gally;
    private int virag;

    public int getRonk() {
        return ronk;
    }

    public void setRonk(int ronk) {
        this.ronk = ronk;
    }

    public int getKo() {
        return ko;
    }

    public void setKo(int ko) {
        this.ko = ko;
    }

    public int getFu() {
        return fu;
    }

    public void setFu(int fu) {
        this.fu = fu;
    }

    public int getGally() {
        return gally;
    }

    public void setGally(int gally) {
        this.gally = gally;
    }

    public int getVirag() {
        return virag;
    }

    public void setVirag(int virag) {
        this.virag = virag;
    }

    public Anyagok(){
        this.ronk = 0;
        this.ko = 0;
        this.fu = 0;
        this.gally = 0;
        this.virag = 0;
    }


}
